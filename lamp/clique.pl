%%
%% generate a problem of size N.
%%
generate_problem(N, Matrix) :-
        numlist(1,N,Is),
        maplist(generate_problem_(N),Is,Matrix).
generate_problem_(N, _, Row) :-
        findall(R,
                (between(1,N,_),
                 random_between(0,1,R)
                ),
                Row).

%%
%% clique(G, Clique, Card, Type)
%%
%% Find a clique in graph G with cardinality Card.
%% Type: - 'all': yield any solution
%%       - ground(Card): yield a solution with cardinality Card
%%       - else: optimize Card (max Cardinality)
%%  
%% As we see in go5/0, clique/4 is reversible (at least kind of).
%%
clique(G, Clique, Card, Type) :-

        length(G,NumNodes),

        %% The found clique (as 0..1)
        %% This will be converted to a node list after solve.
        length(CliqueBool, NumNodes),
        CliqueBool ins 0..1,
        
        %% cardinality of the clique
        sum(CliqueBool,#=,Card),
        Card in 1..NumNodes,        


        %% If there is a connection between nodes I and J (I \= J) then
        %% there should be a node from I to J.
        numlist(1,NumNodes,Is),
        numlist(1,NumNodes,Js),        
        maplist(connection_nodes(G,CliqueBool,Js),Is),        

        %% What we do depends on the modes of the call:
        %%  If Type = all or Card is ground (i.e. the cardinality is instantiated)
        %%  then we show all cliques (of this cardinality).
        %%  Else we search for the largest clique.
        flatten([CliqueBool,Card], Vars),        
        (
            (Type == all ; ground(Card))
        ->
            labeling([ffc,down],Vars)
        ;
            labeling([ffc,down,bisect,max(Card)], Vars)
        ),
        %% Translate boolean -> numbers
        findall(I,
                (between(1,NumNodes,I),
                 nth1(I,CliqueBool,1)
                ),
                Clique).


%%
%% This is kind of backward logic but it is the whole thing,
%% and it works:
%% If there is a connection between nodes I and J (I \= J) then
%% there should be a node from I to J.
%%
connection_nodes(G,Clique,Js,I) :-
        nth1(I,Clique,CI),
        maplist(connection_nodes_(G,Clique,I,CI),Js).

connection_nodes_(G,Clique,I,CI,J) :-
        (I #\= J
        -> 
         matrix_element5(G,I,J,GIJ),
         nth1(J,Clique,CJ),
         (CI + CJ #= 2) #==> (GIJ #> 0)
        ;
         true
        ).

